    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String idRolText = JOptionPane.showInputDialog(null, "Ingrese el ID del rol a eliminar:");
if (idRolText == null || idRolText.isEmpty()) {
    return; // Salir del m√©todo si no se ingresa un ID v√°lido
}
try {
    int idRol = Integer.parseInt(idRolText);

    // Obtener los datos del rol a eliminar
    Connection connection = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=Mariscos", "anyel", "admin");
    String query = "SELECT * FROM Roles WHERE id_rol = ?";
    PreparedStatement statement = connection.prepareStatement(query);
    statement.setInt(1, idRol);
    ResultSet resultSet = statement.executeQuery();

    // Verificar si se encontr√≥ el rol
    if (resultSet.next()) {
        // Obtener los datos del rol
        String nombreRol = resultSet.getString("nombre");

        // Mostrar los datos del rol en un JOptionPane
        String mensajeConfirmacion = "¬øDesea eliminar el siguiente rol?\n\n";
        mensajeConfirmacion += "ID: " + idRol + "\n";
        mensajeConfirmacion += "Nombre: " + nombreRol + "\n";
        int confirmacion = JOptionPane.showConfirmDialog(null, mensajeConfirmacion, "Confirmar eliminaci√≥n", JOptionPane.YES_NO_OPTION);
        if (confirmacion == JOptionPane.YES_OPTION) {
            try {
                // Realizar la eliminaci√≥n del rol
                String deleteQuery = "DELETE FROM Roles WHERE id_rol = ?";
                PreparedStatement deleteStatement = connection.prepareStatement(deleteQuery);
                deleteStatement.setInt(1, idRol);
                int filasAfectadas = deleteStatement.executeUpdate();

                if (filasAfectadas > 0) {
                    JOptionPane.showMessageDialog(null, "Rol eliminado exitosamente");
                } else {
                    JOptionPane.showMessageDialog(null, "No se encontr√≥ ning√∫n rol con el ID especificado");
                }

                deleteStatement.close();
            } catch (SQLException e) {
                // Mostrar un mensaje de error si ocurre una excepci√≥n
                JOptionPane.showMessageDialog(null, "Error al eliminar el rol");
            }
        }
    } else {
        JOptionPane.showMessageDialog(null, "No se encontr√≥ ning√∫n rol con el ID especificado");
    }

    // Cerrar la conexi√≥n y liberar recursos
    resultSet.close();
    statement.close();
    connection.close();

} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(null, "El ID del rol debe ser un n√∫mero entero v√°lido");
} catch (SQLException e) {
    JOptionPane.showMessageDialog(null, "Error al acceder a la base de datos");
    e.printStackTrace();
}
    cargarDatosTabla();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
 String nombreBusqueda = JOptionPane.showInputDialog(null, "Ingrese el nombre a buscar:");
        if (nombreBusqueda == null || nombreBusqueda.isEmpty()) {
    return; // Salir del m√©todo si no se ingresa un nombre v√°lido
}

try {
    Connection connection = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=Mariscos", "anyel", "admin");
    String query = "SELECT id_rol, nombre FROM Roles WHERE nombre LIKE ?";
    PreparedStatement statement = connection.prepareStatement(query);
    statement.setString(1, "%" + nombreBusqueda + "%");
    ResultSet resultSet = statement.executeQuery();

    // Obtener los metadatos del resultado de la consulta
    ResultSetMetaData metaData = resultSet.getMetaData();

    // Obtener el n√∫mero de columnas
    int numColumnas = metaData.getColumnCount();

    // Limpiar la tabla
    DefaultTableModel model = (DefaultTableModel) tablaRol.getModel();
    model.setRowCount(0);

    // Recorrer el resultado de la consulta y agregar las filas a la tabla
    while (resultSet.next()) {
        Object[] fila = new Object[numColumnas];
        for (int i = 1; i <= numColumnas; i++) {
            fila[i - 1] = resultSet.getObject(i);
        }
        model.addRow(fila);
    }

    // Cerrar el resultado, la declaraci√≥n y la conexi√≥n
    resultSet.close();
    statement.close();
    connection.close();

    // Mostrar un mensaje si no se encontraron resultados
    if (model.getRowCount() == 0) {
        JOptionPane.showMessageDialog(null, "No se encontraron registros con el nombre especificado");
    }

} catch (SQLException e) {
    e.printStackTrace();
    // Mostrar un mensaje de error si ocurre una excepci√≥n
    JOptionPane.showMessageDialog(null, "Error al realizar la b√∫squeda");
}
    
    }//GEN-LAST:event_btnBuscarActionPerformed

