private boolean existeRol(int idRol) {
    boolean existe = false;

    try {
        Connection connection = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=Mariscos", "anyel", "admin");
        PreparedStatement statement = connection.prepareStatement("SELECT COUNT(*) AS count FROM Roles WHERE id_rol = ?");
        statement.setInt(1, idRol);
        ResultSet resultSet = statement.executeQuery();

        if (resultSet.next()) {
            int count = resultSet.getInt("count");
            existe = (count > 0);
        }

        resultSet.close();
        statement.close();
        connection.close();
    } catch (SQLException e) {
        e.printStackTrace();
    }

    return existe;
}

    private void bttGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttGuardarActionPerformed
        int idRol;
    String idRolText = txtID.getText().trim();
    if (idRolText.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Ingrese el ID del rol");
        return;
    }
    try {
        idRol = Integer.parseInt(idRolText);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "El ID del rol debe ser un n√∫mero entero v√°lido");
        return; // Salir del m√©todo si el ID del rol no es v√°lido
    }

    // Validar nombre del rol
    String nombre = txtNombre.getText().trim();
    if (nombre.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Ingrese el nombre del rol");
        return;
    }

    // Validar existencia del ID del rol
    if (existeRol(idRol)) {
        JOptionPane.showMessageDialog(null, "El ID del rol ya existe");
        return; // Salir del m√©todo si el ID del rol ya existe
    }

    try {
        Connection connection = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=Mariscos", "anyel", "admin");
        String query = "INSERT INTO Roles (id_rol, nombre) VALUES (?, ?)";
        PreparedStatement statement = connection.prepareStatement(query);
        statement.setInt(1, idRol);
        statement.setString(2, nombre);

        statement.executeUpdate();

        statement.close();
        connection.close();

        // Mostrar un mensaje de √©xito
        JOptionPane.showMessageDialog(null, "Rol agregado correctamente");

        // Limpiar los campos de texto
        txtID.setText("");
        txtNombre.setText("");

        // Cargar los datos actualizados en la tabla
        cargarDatosTabla();

    } catch (SQLException e) {
        e.printStackTrace();
        // Mostrar un mensaje de error si ocurre una excepci√≥n
        JOptionPane.showMessageDialog(null, "Error al agregar el rol");
    }

    }//GEN-LAST:event_bttGuardarActionPerformed

